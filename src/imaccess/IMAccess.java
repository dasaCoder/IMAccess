    int getIDOfAcedamicYear(String s){
        for(AcedamicYear acYear:acedamicYearList){
            if(s.equals(acYear.getAcademic_year())){
                return acYear.getId();
            }
            
        }
        return -99;
    }
    
    int getIDOfDegree(String s){
        for(Degree dgr:degreeList){
            if(s.equals(dgr.getShort_name())){
                return dgr.getId();
            }
            
        }
        return -99;
    }

    
